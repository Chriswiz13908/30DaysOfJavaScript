const PI = Math.PI
console.log(PI)
console.log(Math.round(PI))  // round values to nearest number
console.log(Math.round(9.81))
console.log(Math.floor(PI))  // round down
console.log(Math.ceil(PI))  // round up
console.log(Math.min(-5, 3, 20, 4, 5, 10))  // returns the minimum value
console.log(Math.max(-5, 3, 20, 4, 5, 10))  // returns max value

const randNum = Math.random()  // creates random number between 0 to 0.999999
console.log(randNum)

const num = Math.floor(Math.random() * 11)  // creates random number between 0 to 10
console.log(num)

// Absolute value
console.log(Math.abs(-10))

// Square root
console.log(Math.sqrt(100))

// Power or exponent
console.log(Math.pow(3, 2))  // 3 to the power of 2
console.log(Math.E)

// Logarithm - Returns the natural logarithm of base E of x, Math.log(x)
console.log(Math.log(2)) 
console.log(Math.log(10))

// Trigonometry
console.log(Math.sin(0))
console.log(Math.sin(60))
console.log(Math.cos(0))
console.log(Math.cos(60))